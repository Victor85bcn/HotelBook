    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

        //Comprobación de que los elementos contienen los datos requeridos.
        if (txtnombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes introducir un nombre para el trabajador.");
            txtnombre.requestFocus();
            return;
        }
        if (txtapellido1.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes introducir un apellido para el trabajador.");
            txtapellido1.requestFocus();
            return;
        }
        if (txtapellido2.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes introducir un apellido para el trabajador.");
            txtapellido2.requestFocus();
            return;
        }
        
        if (txtnum_documento.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes introducir un número de documento para el trabajador.");
            txtnum_documento.requestFocus();
            return;
        }
        if (txtsueldo.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes introducir un sueldo para el trabajador.");
            txtsueldo.requestFocus();
            return;
        }
        if (txtlogin.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes introducir un login para el trabajador.");
            txtlogin.requestFocus();
            return;
        }
        if (txtpassword.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes introducir un password para el trabajador.");
            txtpassword.requestFocus();
            return;
        }
        //Fin de la comprobación.

        vtrabajador dts = new vtrabajador(); //Inicializa el objeto vtrabajador.
        ftrabajador func = new ftrabajador(); //Inicializa el objeto vtrabajador.

        dts.setNombre(txtnombre.getText()); //introduce el valor número en el objeto.
        dts.setApellido1(txtapellido1.getText());
        dts.setApellido2(txtapellido2.getText());
        
        int seleccionado = cbotipo_documento.getSelectedIndex();
        dts.setTipo_documento((String) cbotipo_documento.getItemAt(seleccionado));
        
        dts.setNum_documento(txtnum_documento.getText());
        dts.setDireccion(txtdireccion.getText());
        dts.setTelefono(txttelefono.getText());
        dts.setEmail(txtemail.getText());
        dts.setSueldo(Double.parseDouble(txtsueldo.getText())); 
        
        seleccionado = cboacceso.getSelectedIndex();
        dts.setAcceso((String) cboacceso.getItemAt(seleccionado));
        
        dts.setLogin(txtlogin.getText());
        dts.setPassword(txtpassword.getText());
        
        seleccionado = cboestado.getSelectedIndex();
        dts.setEstado((String) cboestado.getItemAt(seleccionado));
        

        if (accion.equals("guardar")) { //Comprueba la instrucción ha realizar
            if (func.insertar(dts)) { //Guarda la habitación.
                JOptionPane.showMessageDialog(rootPane, "El trabajador se ha registrado correctamente.");
                mostrar("");
                inhabilitar();
            }

        } else if (accion.equals("editar")) {
            dts.setIdpersona(Integer.parseInt(txtidpersona.getText()));

            if (func.editar(dts)) { //Modifica el cliente.
                JOptionPane.showMessageDialog(rootPane, "El trabajador se ha modificado correctamente.");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

