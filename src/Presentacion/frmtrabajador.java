    void mostrar(String buscar) {

        //INPUT: String de busqueda.
        //OUTPUT: Nada.
        //DESC: Función que mostrará el resultado de la búsqueda del usuario.
        try {
            
            DefaultTableModel modelo;
            ftrabajador func = new ftrabajador(); //Instancia de la clase fcliente para utilizar sus métodos.
            
            modelo = func.mostrar(buscar); //Ejecuta el método mostrar en modelo.
            
            tablalistado.setModel(modelo); //Añade la información obtenida a tablalistado.
            ocultar_columnas(); //Oculta las columnas no deseadas como id_cliente.
            lbltotalregistros.setText("Total trabajadores:" + Integer.toString(func.totalregistros)); //Calcula el total de registros encontrados.
            
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, e);
        }
    }

